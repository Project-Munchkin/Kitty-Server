package com.munchkin.product.dto;

public class SizeDto {
    private String XS;
    private String S;
    private String M;
    private String L;
    private String XL;

	public String getXS() {
		return XS;
	}
	public void setXS(String xS) {
		XS = xS;
	}
	public String getS() {
		return S;
	}
	public void setS(String s) {
		S = s;
	}
	public String getM() {
		return M;
	}
	public void setM(String m) {
		M = m;
	}
	public String getL() {
		return L;
	}
	public void setL(String l) {
		L = l;
	}
	public String getXL() {
		return XL;
	}
	public void setXL(String xL) {
		XL = xL;
	}

}
